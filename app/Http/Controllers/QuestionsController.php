     * @return \Illuminate\Http\Response
     */
    public function store(QuestionRequest $request)
    {
        //
        Question::create([
               'title'=> $request->title,
               'body'=>$request->body,
               'slug'=> str_slug($request->title),
               'category_id'=> $request->category,
               'user_id'=>auth()->user()->id
        ]);
        return redirect('/home')->withSuccess('Question added');
    }

